public class Main 
{
  public static void main(String[] args)
  {
    final int a = 1; //cant be changed
    double b = 3.4; 
    boolean c = true;

    System.out.print("A: ");
    System.out.println(a);
    System.out.print("B: ");
    System.out.println(b);
    System.out.print("C: ");
    System.out.println(c);

    System.out.print("A + B: ");
    System.out.println(a + b); //implicit casting

    
  }
}
